# 07_numpy_basics.py
import numpy as np  # 习惯性地把 numpy 简写为 np，这是全世界程序员的默契

# =====================================================================
# 1. 创建高维阵列 (模拟一张带有物理噪声的 4x4 极简深度相机图像)
# =====================================================================
print("--- 📸 1. 创建 NumPy 高维阵列 (NDArray) ---")

# 假设 999.0 是深度相机因为反光抓拍到的“过载噪声点”
fake_depth_image = np.array([
    [1.2, 1.5, 999.0, 1.8],
    [2.0, 1.1, 1.4,   2.2],
    [0.8, 999.0, 1.3, 1.7],
    [2.5, 2.1, 1.9,   1.6]
])

print(f"原始深度图矩阵:\n{fake_depth_image}")
# .shape 属性可以让你瞬间看穿这个矩阵的长宽结构
print(f"图像的初始形状 (Shape): {fake_depth_image.shape}") # 输出 (4, 4) 代表 4行4列
print("\n" + "="*40 + "\n")


# =====================================================================
# 2. 批量数据清洗 (高仿毕设第 67 行 depth[depth > 10000] = 0)
# =====================================================================
print("--- 🧼 2. NumPy 批量条件筛选与清洗 ---")

# 重点来了！不需要写任何 for 循环！
# 这行命令的潜台词是：“把矩阵里所有正好等于 999.0 的脏格子筛选出来，一瞬间全部抹成 0.0”
fake_depth_image[fake_depth_image == 999.0] = 0.0

print(f"✨ 清洗完盲区噪声后的深度图矩阵:\n{fake_depth_image}")
print("\n" + "="*40 + "\n")


# =====================================================================
# 3. 维度重塑魔法 (高仿毕设第 64 行 image.reshape)
# =====================================================================
print("--- 🔀 3. 矩阵维度重塑 (Reshape) 迎合 AI 模型 ---")

# 你的 AI 策略网络（LoGoPlanner）是一个严格的流水线工人。
# 它规定：进来的图像数据不能只是孤立的二维平面 (4, 4)，必须要在最前面加一个“Batch_size (批次大小)”的维度，变成三维的 (1, 4, 4)
# .reshape() 可以在绝对不改变内部 16 个核心数字的前提下，完美变换空间的揉捏形状！

tensor_style_image = fake_depth_image.reshape((1, 4, 4))

print(f"重塑后的矩阵新形状 (Shape): {tensor_style_image.shape}") # 变成了 (1, 4, 4)
print(f"重塑后的多维阵列内容呈现:\n{tensor_style_image}")